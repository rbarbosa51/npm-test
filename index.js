/** This function adds numbers a + b 
 * @param {number} a
 * @param {number} b
*/
export const add = (a, b) => {
    return a + b;
}

/** This function subtract numbers a - b 
 * @param {number} a
 * @param {number} b
*/
export const subtract = (a, b) => {
    return a - b;
}

/** This function multiplies numbers a * b 
 * @param {number} a
 * @param {number} b
*/
export const multiply = (a, b) => {
    return a * b;
}

